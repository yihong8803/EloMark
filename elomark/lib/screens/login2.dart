class HomePage extends StatefulWidget {
  @override
  _HomePageState createState() => _HomePageState();
}

class _HomePageState extends State<HomePage> {
  final TextEditingController _studentIdController = TextEditingController();
  final TextEditingController _passwordController = TextEditingController();
  bool _isLoading = false;
  String? _errorMessage;
  void _loginUser() async {
    setState(() {
      _isLoading = true;
      _errorMessage = null;
    });

    final studentId = _studentIdController.text.trim();
    final password = _passwordController.text.trim();

    // ðŸ” Secret Admin Login
    if (studentId == "12345678" && password == "admin123") {
      setState(() {
        _isLoading = false;
      });

      Navigator.push(
        context,
        MaterialPageRoute(
          builder: (context) => CourseSelectionPage(), // Go to admin page
        ),
      );
      return;
    }

    try {
      final response = await StudentService.login(studentId, password);

      setState(() {
        _isLoading = false;
      });

      if (response.error == null && response.data != null) {
        final student = response.data!;
        Navigator.push(
          context,
          MaterialPageRoute(
            builder:
                (context) =>
                    StudentMarkPage(studentId: student.studentId.toString()),
          ),
        );
      } else {
        setState(() {
          _errorMessage = response.error ?? "Unknown error";
        });
      }
    } catch (e) {
      setState(() {
        _isLoading = false;
        _errorMessage = "Something went wrong: $e";
      });
    }
  }

